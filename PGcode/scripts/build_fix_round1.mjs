// ─── Helper: SQL-escape a python string for embedding ───
const sq = (s) => "'" + s.replace(/'/g, "''") + "'";
const jb = (obj) => sq(JSON.stringify(obj)) + '::jsonb';

// ─── Helper: emit solution code update ───
function setSolution(id, code) {
  out.push(`-- ${id}: solution code`);
  out.push(`UPDATE public."PGcode_solution_approaches"`);
  out.push(`   SET code_python = ${sq(code)}`);
  out.push(` WHERE problem_id = '${id}' AND approach_number = 1;`);
  out.push('');
}

// ─── Helper: emit single test case expected fix ───
function fixExpected(id, idx0, newExpected) {
  out.push(`-- ${id}: test #${idx0 + 1} expected`);
  out.push(`UPDATE public."PGcode_problems"`);
  out.push(`   SET test_cases = jsonb_set(test_cases, '{${idx0},expected}', ${sq(JSON.stringify(newExpected))}::jsonb)`);
  out.push(` WHERE id = '${id}';`);
  out.push('');
}

// ─── Helper: emit single test case full replacement (e.g. inputs+expected) ───
function replaceCase(id, idx0, tcObj) {
  out.push(`-- ${id}: replace test #${idx0 + 1}`);
  out.push(`UPDATE public."PGcode_problems"`);
  out.push(`   SET test_cases = jsonb_set(test_cases, '{${idx0}}', ${jb(tcObj)})`);
  out.push(` WHERE id = '${id}';`);
  out.push('');
}

// ─── Helper: replace entire test_cases array ───
function setAllCases(id, cases) {
  out.push(`-- ${id}: replace all test_cases (sorted to match canonical solution)`);
  out.push(`UPDATE public."PGcode_problems"`);
  out.push(`   SET test_cases = ${jb(cases)}`);
  out.push(` WHERE id = '${id}';`);
  out.push('');
}

// ─── Helper: metadata update ───
function setMeta(id, fields) {
  const sets = Object.entries(fields).map(([k, v]) => {
    if (typeof v === 'string') return `${k} = ${sq(v)}`;
    return `${k} = ${jb(v)}`;
  }).join(', ');
  out.push(`-- ${id}: metadata`);
  out.push(`UPDATE public."PGcode_problems" SET ${sets} WHERE id = '${id}';`);
  out.push('');
}

