#!/usr/bin/env node
// Generate C++ starter templates for every problem in PGcode_problems and
// emit a SQL seed file (scripts/seed-18-cpp-templates.sql) that inserts them
// into PGcode_problem_templates with language='cpp'.
//
// Usage (from repo root):
//   node scripts/seed-cpp-templates.mjs
//
// Reads VITE_SUPABASE_URL / VITE_SUPABASE_ANON_KEY from .env (SELECT only).
// The produced SQL is idempotent — uses INSERT ... ON CONFLICT DO UPDATE.

import { readFileSync, writeFileSync } from 'fs';
import { fileURLToPath } from 'url';
import { dirname, join } from 'path';
import { generateTemplate } from '../src/lib/driverCode.js';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');

const env = Object.fromEntries(
  readFileSync(join(ROOT, '.env'), 'utf8')
    .split('\n')
    .filter(l => l && !l.startsWith('#'))
    .map(l => {
      const i = l.indexOf('=');
      return [l.slice(0, i).trim(), l.slice(i + 1).trim()];
    })
);

const SUPABASE_URL = env.VITE_SUPABASE_URL;
const ANON_KEY = env.VITE_SUPABASE_ANON_KEY;
if (!SUPABASE_URL || !ANON_KEY) throw new Error('Missing VITE_SUPABASE_URL / VITE_SUPABASE_ANON_KEY in .env');

// Fetch problems in pages — PostgREST caps rows-per-request.
async function fetchAllProblems() {
  const all = [];
  const PAGE = 1000;
  for (let offset = 0; ; offset += PAGE) {
    const url = `${SUPABASE_URL}/rest/v1/PGcode_problems?select=id,method_name,params,return_type&limit=${PAGE}&offset=${offset}`;
    const res = await fetch(url, {
      headers: {
        'apikey': ANON_KEY,
        'Authorization': `Bearer ${ANON_KEY}`,
      },
    });
    if (!res.ok) throw new Error(`SELECT failed: ${res.status} ${await res.text()}`);
    const rows = await res.json();
    all.push(...rows);
    if (rows.length < PAGE) break;
  }
  return all;
}

function sqlEscape(s) {
  // Use dollar-quoted strings to avoid escaping backslashes/quotes in code.
  return `$CPP$${s}$CPP$`;
}

const problems = await fetchAllProblems();
console.error(`Fetched ${problems.length} problems`);

const rows = [];
let skipped = 0;
for (const p of problems) {
  if (!p.method_name || !p.params) { skipped++; continue; }
  let params = p.params;
  if (typeof params === 'string') {
    try { params = JSON.parse(params); } catch { skipped++; continue; }
  }
  const tmpl = generateTemplate('cpp', p.method_name, params, p.return_type);
  if (!tmpl) { skipped++; continue; }
  rows.push(`  ('${p.id.replace(/'/g, "''")}', 'cpp', ${sqlEscape(tmpl)})`);
}
console.error(`Emitted ${rows.length} rows, skipped ${skipped}`);

const header = `-- Auto-generated by scripts/seed-cpp-templates.mjs
-- C++ starter templates for every problem with method_name + params metadata.
-- Safe to re-run: ON CONFLICT updates in place.

INSERT INTO public."PGcode_problem_templates" (problem_id, language, code) VALUES
`;
const footer = `
ON CONFLICT (problem_id, language) DO UPDATE SET code = EXCLUDED.code;
`;

const sql = header + rows.join(',\n') + footer;
const outPath = join(ROOT, 'scripts/seed-18-cpp-templates.sql');
writeFileSync(outPath, sql);
console.error(`Wrote ${outPath} (${sql.length.toLocaleString()} bytes)`);
